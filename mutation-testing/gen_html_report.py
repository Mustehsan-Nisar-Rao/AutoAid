import re
from datetime import datetime

# Read the results file
with open('mutation_results.txt', 'r') as f:
    content = f.read()

# Count killed and survived
killed = len(re.findall(r': killed', content))
survived = len(re.findall(r': survived', content))
total = killed + survived
score = round(killed / total * 100, 1) if total > 0 else 0

# Create HTML
html = f"""<!DOCTYPE html>
<html>
<head>
    <title>Mutation Testing Report - AutoAid</title>
    <meta charset="UTF-8">
    <style>
        body {{ font-family: Arial, sans-serif; margin: 40px; background: #f5f5f5; }}
        .container {{ max-width: 1000px; margin: auto; background: white; padding: 30px; border-radius: 10px; }}
        h1 {{ color: #2c3e50; border-bottom: 3px solid #3498db; padding-bottom: 10px; }}
        .summary {{ display: flex; gap: 20px; margin: 30px 0; }}
        .card {{ background: #f8f9fa; padding: 20px; border-radius: 10px; flex: 1; text-align: center; }}
        .card .number {{ font-size: 48px; font-weight: bold; }}
        .killed .number {{ color: #27ae60; }}
        .survived .number {{ color: #e74c3c; }}
        .score .number {{ color: #3498db; }}
        pre {{ background: #f8f9fa; padding: 15px; border-radius: 8px; overflow-x: auto; font-size: 12px; }}
        .footer {{ margin-top: 30px; text-align: center; color: #7f8c8d; font-size: 12px; }}
    </style>
</head>
<body>
<div class="container">
    <h1>🧬 Mutation Testing Report</h1>
    <p><strong>Project:</strong> AutoAid Recommender API</p>
    <p><strong>Date:</strong> {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}</p>

    <div class="summary">
        <div class="card"><h3>Total Mutants</h3><div class="number">{total}</div></div>
        <div class="card killed"><h3>✅ Killed</h3><div class="number">{killed}</div></div>
        <div class="card survived"><h3>❌ Survived</h3><div class="number">{survived}</div></div>
        <div class="card score"><h3>🎯 Mutation Score</h3><div class="number">{score}%</div></div>
    </div>

    <h2>📊 Full Results</h2>
    <pre>{content}</pre>

    <div class="footer">
        <p>Generated by mutmut | AutoAid Mutation Testing Assignment</p>
    </div>
</div>
</body>
</html>"""

# Create directory if not exists
import os
os.makedirs('reports/mutation_baseline', exist_ok=True)

# Write HTML file
with open('reports/mutation_baseline/index.html', 'w') as f:
    f.write(html)

print(f'✅ HTML Report generated at reports/mutation_baseline/index.html')
print(f'   Total: {total} | Killed: {killed} | Survived: {survived} | Score: {score}%')
